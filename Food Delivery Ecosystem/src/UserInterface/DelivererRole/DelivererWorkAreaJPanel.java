    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        int r = this.tblWork.getSelectedRow();
        FoodStorageEnterprise fs = (FoodStorageEnterprise)cbFoodStorage.getSelectedItem();
        if(r>=0){
            WorkRequest wq = (WorkRequest)tblWork.getValueAt(r, 1);
            wq.setStatus("Delivered");
            wq.setResolveDate(new Date());
            wq.getFood().setFrom(enterprise);
            fs.getFoodlist().getFoodlist().add(wq.getFood());
            JOptionPane.showMessageDialog(null, r);
        }
    }//GEN-LAST:event_btnProcessActionPerformed

