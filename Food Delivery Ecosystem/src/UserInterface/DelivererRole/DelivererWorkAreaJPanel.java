    public void popCom(){
        this.cbFoodStorage.removeAllItems();;
        for(Enterprise e : city.getEnterpriseDir().getEnterpriseList()){
            if(e.getEnterpriseType() == Enterprise.EnterpriseType.FoodStorage){
                cbFoodStorage.addItem(e);
            }
        }
    }
    public void popCom2(refrigerator re){
        this.WRCbx.removeAllItems();
        for(drawer d:re.getDrawers()){
                WRCbx.addItem(d);
        }
    }
    public void popTbl2(){
        DefaultTableModel dtm2 = (DefaultTableModel)tblWork2.getModel();
        dtm2.setRowCount(0);
        for(WorkRequest wq:this.city.getWorkqueue().getWorkRequestList()){
            if(wq.getWorktype()==WorkRequest.workType.Deliver && wq.getReceiver()== this.useraccount && wq.getStatus()=="Undelivered"&&((DeliverWorkRequest)wq).hasRef()){
                Object[] row2 =new Object[3];
                row2[0]=wq.getWorkId();
                row2[1]=wq.getFood();
                row2[2]=((DeliverWorkRequest)wq).getRefrigerator();
                dtm2.addRow(row2);
            }
    }
    }
    public void popTbl(){
        DefaultTableModel dtm = (DefaultTableModel)tblWork.getModel();
        dtm.setRowCount(0);
        for(WorkRequest wq:this.city.getWorkqueue().getWorkRequestList()){
            if(wq.getWorktype()==WorkRequest.workType.Deliver && wq.getReceiver()== this.useraccount && wq.getStatus()=="Undelivered"){
                Object[] row = new Object[5];
                row[0]=wq.getFood().getFoodId();
                row[1]=wq;
                row[2]=wq.getFood().getDonor();
                row[3]=wq.getFood().getExpirationDate();
                row[4]=wq.getFood().getFrom();
                dtm.addRow(row);
            }
        }
    }

