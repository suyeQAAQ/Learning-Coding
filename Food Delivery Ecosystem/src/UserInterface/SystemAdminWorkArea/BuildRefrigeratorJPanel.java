    private void buildBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buildBtnActionPerformed
        // TODO add your handling code here:
        boolean build = true;
        try{
            Integer.parseInt(txtUnit.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please enter valid number type!");
            build = false;
        }
        if(build){
            String street = txtStreet.getText();
        int unit = Integer.parseInt(txtUnit.getText());
        String address = unit + street + " St";
        refrigerator r = network.getRefriDir().createNewRefrigerator(address);
        popTbl();
        JOptionPane.showMessageDialog(null, "The refrigerator has been settled at "+ address);
        txtStreet.setText("");
        txtUnit.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "Unable to build this refrigerator!");
        }
    }//GEN-LAST:event_buildBtnActionPerformed

