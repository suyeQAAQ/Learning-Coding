    public void popTbl(){
        DefaultTableModel dtm = (DefaultTableModel)tblRefri.getModel();
        dtm.setRowCount(0);
        for(refrigerator r:network.getRefriDir().getRefrilist()){
            Object[] row = new Object[2];
            row[0] = r.getRefrigeratorId();
            row[1] = r;
            dtm.addRow(row);
        }
    }

